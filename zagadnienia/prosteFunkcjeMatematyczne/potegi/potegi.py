# Funkcje definiowane przez użytkownika
def potegowanieDoDrugiej(n):

    return n * n

def potegowanieDoTrzeciej(n):

    return n ** 3


print(potegowanieDoDrugiej(3))
print(potegowanieDoTrzeciej(3))

